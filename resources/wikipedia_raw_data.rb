                    ===Expo '58 and posterity===\n
                    Since opening, only six of the nine spheres are accessible to the public, the three of the central axis and the three lower ones,<ref name=\":0\" /> each with two main floors and a lower floor reserved for service. Tubes of {{convert|3|m|sigfig=1}} diameter connect the spheres along the twelve edges of the cube and all eight vertices to the centre. The central tube contains the fastest [[elevator]] in Europe of the time with a speed of {{convert|5|m/s || abbr=on|sigfig=1}},<ref name=\":0\">{{Cite web|title=Atomium – Inventaire du patrimoine architectural|url=https://monument.heritage.brussels/fr/Bruxelles_Laeken/Square_de_l_Atomium/A001/38328|access-date=2021-11-21|website=monument.heritage.brussels|language=fr}}</ref> installed by the Belgian branch of the Swiss firm [[Schweizerische Wagons- und Aufzügefabrik AG Schlieren-Zürich|Schlieren]] (subsequently taken over by [[Schindler Group|Schindler]]). It allows 22 people to reach the summit in 23 seconds. The [[escalator]]s installed in the oblique tubes are also among the longest in Europe. The biggest is {{convert|35|m|ft|sigfig=1}} long.\n\n

                    Three of the four top spheres lack vertical support and hence are not open to the public for safety reasons (to minimise foot traffic in the sphere). The original design called for no leg supports on the three lower outer spheres; the structure was simply to rest on the singular mid-bottom sphere. However, [[wind tunnel]] tests proved that such a structure would have toppled in an {{convert|80|km/h|abbr=on|sigfig=1}} wind, whilst up to {{convert|140|km/h|abbr=on|sigfig=1}} winds have been recorded in Belgium. Support columns were thus added under the three spheres in question to achieve enough resistance against overturning.<ref>{{cite web|url=http://en.structurae.de/structures/data/index.cfm?ID=s0000887|title=Atomium (1958) &#124; Structurae|date=24 February 2006|publisher=En.structurae.de|language=de|access-date=5 July 2012}}</ref>\n\n

                    The Atomium, designed to last six months, was not destined to survive the 1958 World's Fair, but its popularity and success made it a major element of Brussels' landscape.<ref name=\":0\" /> Its destruction was therefore postponed year after year, until the city's authorities decided to keep it. However, for thirty years, little maintenance work was done. Still, the building continued serving as the backdrop for major events and competitions. In 1960, the [[1960 Tour de France|47th Tour de France]] passed in front of it on its inaugural stages. From 1960 to 1962, the [[:fr:Grand Prix automobile de Bruxelles|Brussels Motor Grand Prix]] automobile race was organised not far from it.\n\n

