        === Renovation (2004–2006) ===\n
        By the turn of the millennium, the state of the building had deteriorated and a comprehensive renovation was sorely needed. Renovation of the Atomium, carried by Belgian construction companies Jacques Delens and BESIX, began in March 2004; it was closed to the public in October of that year, and remained closed until 18 February 2006. The renovation included replacing the faded aluminium sheets on the spheres with stainless steel, as well as building a new reception pavilion with a boomerang-shaped roof. At the foot of the building, the roundabout was redeveloped into a concrete esplanade lined with continuous benches and a large step leading to the north-east.On 21 December 2005, the Atomium's new outdoor lighting was tested. The meridians of each sphere were covered with rectangular steel plates, in which LED lighting was integrated. The LED application illuminates the bulbs at night. The lights can also flash simultaneously or in turns at each meridian, symbolising the range of an electron around its core. In addition, the German industrial designer Ingo Maure created lighting objects and installations for the interior of the building.On 14 February 2006, the Atomium was officially reopened by then-Prince Philippe, and on 18 February 2006, it opened again to the public. The renovation cost €26 million. Brussels and the Atomium Association paid one-third of the costs, and the Belgian Government financed the other two thirds. To help finance the renovation, pieces of the old aluminium plates were sold to the public as souvenirs. One triangular piece about 2 metres (7 ft) long sold for €1,000. In March 2006, a 2 euro commemorative coin depicting the building was issued to celebrate the renovation and reopening.\n
        Though the Atomium depicts an iron unit cell, the spheres were originally clad with aluminium. Following the 2004–2006 renovation, however, the aluminium was replaced with stainless steel, which is primarily iron. Likewise, while the subject of Atomium was chosen to depict the enthusiasm of the Atomic Age, iron is not and cannot be used as fuel in nuclear reactions.\n\n

