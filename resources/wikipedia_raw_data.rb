                    ==Usage==\n

                    The Atomium, with over 600,000 visitors per year, is now the most popular tourist attraction in the capital of Europe, an art centre and an international symbol of Brussels and Belgium. In addition to its heritage value, it is also a cultural place. Over half of the building is dedicated to exhibitions with themes about Belgium and [[digital art]]s.<ref name=\"Official website\" />\n\n

                    Of the six spheres accessible to the public:<ref>{{Cite web|url=http://atomium.be/Plan.aspx?lang=en|title=Atomium|website=atomium.be|access-date=2018-11-25}}</ref>\n
                    * the bottom sphere is reserved for permanent exhibitions dedicated to the 1950s, Expo '58 and the building's construction;\n
                    * the second sphere hosts temporary exhibitions;\n
                    * the third and central spheres have a versatile vocation and allow the organisation of various events, films, concerts, parties or conferences;\n
                    * the top sphere, in addition to the panorama, holds a restaurant;\n
                    * the sixth sphere is the kids' sphere, intended for the organisation of workshops of urban pedagogy, allowing children from six to twelve years to spend the night there.\n\n

