        === Expo '58 and posterity ===\n
        Since opening, only six of the nine spheres are accessible to the public, the three of the central axis and the three lower ones, each with two main floors and a lower floor reserved for service. Tubes of 3 metres (10 ft) diameter connect the spheres along the twelve edges of the cube and all eight vertices to the centre. The central tube contains the fastest elevator in Europe of the time with a speed of 5 m/s (20 ft/s), installed by the Belgian branch of the Swiss firm Schlieren (subsequently taken over by Schindler). It allows 22 people to reach the summit in 23 seconds. The escalators installed in the oblique tubes are also among the longest in Europe. The biggest is 35 metres (100 ft) long.\n
        Three of the four top spheres lack vertical support and hence are not open to the public for safety reasons (to minimise foot traffic in the sphere). The original design called for no leg supports on the three lower outer spheres; the structure was simply to rest on the singular mid-bottom sphere. However, wind tunnel tests proved that such a structure would have toppled in an 80 km/h (50 mph) wind, whilst up to 140 km/h (90 mph) winds have been recorded in Belgium. Support columns were thus added under the three spheres in question to achieve enough resistance against overturning.The Atomium, designed to last six months, was not destined to survive the 1958 World's Fair, but its popularity and success made it a major element of Brussels' landscape. Its destruction was therefore postponed year after year, until the city's authorities decided to keep it. However, for thirty years, little maintenance work was done. Still, the building continued serving as the backdrop for major events and competitions. In 1960, the 47th Tour de France passed in front of it on its inaugural stages. From 1960 to 1962, the Brussels Motor Grand Prix automobile race was organised not far from it.\n\n

