        == Usage ==\n
        The Atomium, with over 600,000 visitors per year, is now the most popular tourist attraction in the capital of Europe, an art centre and an international symbol of Brussels and Belgium. In addition to its heritage value, it is also a cultural place. Over half of the building is dedicated to exhibitions with themes about Belgium and digital arts.Of the six spheres accessible to the public:\n
        the bottom sphere is reserved for permanent exhibitions dedicated to the 1950s, Expo '58 and the building's construction;\n
        the second sphere hosts temporary exhibitions;\n
        the third and central spheres have a versatile vocation and allow the organisation of various events, films, concerts, parties or conferences;\n
        the top sphere, in addition to the panorama, holds a restaurant;\n
        the sixth sphere is the kids' sphere, intended for the organisation of workshops of urban pedagogy, allowing children from six to twelve years to spend the night there.\n\n\n


        == Worldwide copyright claims ==\n\n

        SABAM, Belgium's society for collecting copyrights, has claimed worldwide intellectual property rights on all reproductions of the image via the United States Artists Rights Society (ARS). For example, SABAM issued a demand that a United States website remove all images of the Atomium from its pages. The website responded by replacing all such images with a warning not to take photographs of the Atomium, and that A.S.B.L. Atomium will sue any individual or group if they show the photographs to anyone. SABAM confirmed that permission is required.Ralf Ziegermann remarked on the complicated copyright instructions on the Atomium's website specific to \"private pictures\". The organisers of Belgian heritage, Anno Expo (planning the 50th anniversary celebrations of Expo 58), in the city of Mechelen announced a \"cultural guerrilla strike\" by asking people to send in their old photographs of the Atomium and requested 100 photoshoppers to paint over the balls. SABAM responded that they would make an exception for 2008 and that people could publish private photographs for one year only on condition they were for non-commercial purposes. Anno Expo later announced they had censored part of their own report due to \"complications\" and referred to a meeting they had with SABAM. Mechelen's Mayor, Bart Somers, called the Atomium copyright rules absurd.From the Atomium's website, the current copyright restrictions exempt private individuals under the following conditions:\n
        This is the case where photographs are taken by private individuals and shown on private websites for no commercial purpose (the current trend for photo albums).In accordance with legislation, usage rights for the image of the Atomium would naturally extend to 1 January 2076, in other words, the seventieth anniversary of André Waterkeyn's death.\n
        In the summer of 2015, Belgian political party Open Vld, proposed a bill to enable freedom of panorama in Belgium. The bill was enacted into law in June 2016, allowing pictures of the Atomium, and other public buildings under copyright, to be legally distributed.\n\n\n


