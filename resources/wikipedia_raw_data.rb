        "extract" => "The Atomium ( ə-TOH-mee-əm, French: [atɔmjɔm], Dutch: [aːˈtoːmijəm]) is a landmark modernist building inBrussels, Belgium, originally constructed as the centrepiece of the 1958 Brussels World's Fair (Expo '58). Designed by the engineer André Waterkeyn and the architects André and Jean Polak as a tribute to scientific progress, as well as to symbolise Belgian engineering skills at the time, it is located on the Heysel/Heizel Plateau in Laeken (northern part of the City of Brussels), where the exhibition took place. Nowadays, it is the city's most popular tourist attraction, and serves as a museum, an art centre and a cultural destination.The Atomium stands 102 metres (335 ft) tall, making it one of the tallest structures in Belgium. Its nine 18-metre-diameter (59 ft) stainless steel clad spheres are connected in the shape of a unit cell that could represent an iron crystal magnified 165 billion times. Steel tubes connecting the spheres enclose stairs, escalators and an elevator (in the central, vertical tube) to allow access to the six visitable spheres, which contain exhibit halls and other public spaces. The top sphere includes a restaurant with a panoramic view of Brussels. The building was completely renovated between 2004 and 2006 by the companies Jacques Delens and BESIX.The building is located on the Square de l'Atomium/Atomiumsquare, at the intersection of the Boulevard du Centenaire/Eeuwfeestlaan with the Avenue de l'Atomium/Atomiumlaan and the Avenue de Bouchout/Boechoutlaan, and opposite the Centenary Palace of the Brussels Exhibition Centre (Brussels Expo). It is served by Heysel/Heizel metro station on line 6 of the Brussels Metro.\n\n\n


        == Naming ==\n
        The name Atomium (pronounced  ə-TOH-mee-əm) is a portmanteau combining the words atom and aluminium, the metal with which the spheres were initially covered. The Brussels-Capital Region is bilingual; hence, both the monument's French and Dutch names—l'Atomium and het Atomium—are official. In French, l'Atomium (pronounced [atɔmjɔm]) is used both in the masculine and in the feminine, even if the monument's official team prefers the feminine. In Dutch, the question does not arise since het Atomium (pronounced [aːˈtoːmijəm]) is neuter. In English, it is usually referred to with the definite article 'the' in front: the Atomium.\n\n\n


        == History ==\n\n\n


        === Inception and construction ===\n
        The Atomium was built as the main pavilion and icon of the 1958 Brussels World's Fair (Expo '58). In the 1950s, faith in scientific progress was great, and a structure depicting atoms was chosen to highlight humanity's advance in science, including nuclear physics, which Belgium itself was an avid practitioner ever since the Belgian Congo's uranium became the dominant source of material used for nuclear fission, including that used for the world's first nuclear weapons. With the Atomium, Belgium wanted to highlight and promote the post-war ideal to peacefully apply atomic research and other advancements in technology in order to improve lives and serve the betterment of mankind. The Atomium's nine 18-metre-diameter (59 ft) stainless steel clad spheres depict nine iron atoms in the shape of the body-centred cubic unit cell that could for example represent α-iron (ferrite) crystal, magnified 165 billion times.The construction of the Atomium was a technical feat. In January 1955, a first project was presented by the engineer André Waterkeyn, director of the economic department of Fabrimétal; the Federation of Companies in the Metal Fabricating Industry (now known as Agoria). The architects André and Jean Polak were responsible for the architectural transposition of the concept, drawing up numerous sketches to do so. The company received assistance from consulting engineers Artémy S. Joukoff and André Beckers, assisted by the design office V. Daniel. The foundations were launched in March 1956 and the building, erected by the Jambes-Namur Construction Workshops, was completed less than a month before the inauguration of Expo '58, on 17 April 1958.\n\n

