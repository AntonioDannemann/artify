                    The '''Atomium''' ({{IPAc-en|ə|ˈ|t|oʊ|m|iː|ə|m}} {{respell|ə|TOH|mee-əm}}, {{IPA-fr|atɔmjɔm|lang}}, {{IPA-nl|aːˈtoːmijəm|lang}}) is a [[landmark]]<ref name=\":0\" /> [[Modern architecture|modernist]] building in [[Brussels]], Belgium, originally constructed as the centrepiece of the 1958 Brussels World's Fair ([[Expo '58]]). Designed by the engineer [[André Waterkeyn]] and the architects [[:fr:André et Jean Polak|André and Jean Polak]] as a tribute to scientific progress, as well as to symbolise Belgian engineering skills at the time,<ref name=\"Official website\">[http://www.atomium.be/#/History.aspx Official website]</ref><ref>Le Monde.fr. 2005. André Waterkeyn, concepteur de l'Atomium à Bruxelles. [online] Available at: <https://www.lemonde.fr/disparitions/article/2005/10/07/andre-waterkeyn-concepteur-de-l-atomium-a-bruxelles_696852_3382.html> [Accessed 25 May 2022]. </ref> it is located on the [[Heysel Plateau|Heysel/Heizel Plateau]] in [[Laeken]] (northern part of the [[City of Brussels]]), where the exhibition took place. Nowadays, it is the city's most popular tourist attraction, and serves as a museum, an art centre and a cultural destination.<ref name=\"Official website\"/>\n\n

                    The Atomium stands {{convert|102|m}} tall, making it one of the [[List of tallest structures in Belgium|tallest structures in Belgium]]. Its nine {{convert|18|m|ft|adj=mid|-diameter}} [[stainless steel]] clad [[sphere]]s are connected in the shape of a [[Crystal structure#Unit cell|unit cell]] that could represent an [[iron]] [[crystal]] magnified 165 billion times. Steel tubes connecting the spheres enclose stairs, [[escalator]]s and an [[elevator]] (in the central, vertical tube) to allow access to the six visitable spheres, which contain exhibit halls and other public spaces. The top sphere includes a restaurant with a [[panorama|panoramic]] view of Brussels. The building was completely renovated between 2004 and 2006 by the companies Jacques Delens and [[Besix|BESIX]].<ref>{{Cite web|date=2020-09-24|title=Belgium pavilion to serve up 'belgitude' (with fries)|url=https://www.meed.com/belgium-expo2020-pavilion|access-date=2021-01-14|website=MEED|language=en}}</ref>\n\n

                    The building is located on the {{lang|fr|Square de l'Atomium|italic=no}}/{{lang|nl|Atomiumsquare|italic=no}},<ref name=\"Address\">{{Cite web|title=Square de l'Atomium – Inventaire du patrimoine architectural|url=https://monument.heritage.brussels/fr/0/Square_de_l_Atomium/10208237|access-date=2021-11-20|website=monument.heritage.brussels|language=fr}}</ref><ref>{{Cite web|title=Atomium|url=https://visit.brussels/en/place/Atomium|access-date=2021-11-20|website=visit.brussels|language=en}}</ref> at the intersection of the {{lang|fr|Boulevard du Centenaire|italic=no}}/{{lang|nl|Eeuwfeestlaan|italic=no}} with the {{lang|fr|Avenue de l'Atomium|italic=no}}/{{lang|nl|Atomiumlaan|italic=no}} and the {{lang|fr|Avenue de Bouchout|italic=no}}/{{lang|nl|Boechoutlaan|italic=no}}, and opposite the Centenary Palace of the Brussels Exhibition Centre ([[Brussels Expo]]). It is served by [[Heysel/Heizel metro station]] on [[Brussels Metro line 6|line 6]] of the [[Brussels Metro]].\n\n

                    ==Naming==\n
                    The name ''Atomium'' (pronounced {{IPAc-en|ə|ˈ|t|oʊ|m|iː|ə|m}} {{respell|ə|TOH|mee-əm}}) is a [[portmanteau]] combining the words [[atom]] and [[aluminium]], the metal with which the spheres were initially covered. The Brussels-Capital Region is [[Multilingualism|bilingual]]; hence, both the monument's French and Dutch names—{{lang|fr|l'Atomium}} and {{lang|nl|het Atomium}}—are official. In French, {{lang|fr|l'Atomium}} (pronounced {{IPA-fr|atɔmjɔm|}}) is used both in the masculine and in the feminine, even if the monument's official team prefers the feminine.<ref>{{Citation |title=Atomium : masculin ou féminin? / \"de\" of \"het\"? |url=https://www.youtube.com/watch?v=erZWWN-qFMY |language=en |access-date=2022-04-09}}</ref><ref>{{Cite web |title=Atomium |url=https://twitter.com/atomium/status/405987608884699136 |access-date=2022-04-09 |website=Twitter |language=en}}</ref> In Dutch, the question does not arise since {{lang|nl|het Atomium}} (pronounced {{IPA-nl|aːˈtoːmijəm|}}) is neuter. In English, it is usually referred to with the definite article 'the' in front: ''the Atomium''.<ref name=\"Official website\" />\n\n

                    ==History==\n\n

                    ===Inception and construction===\n
                    The Atomium was built as the main pavilion and icon of the 1958 Brussels World's Fair ([[Expo '58]]). In the 1950s, faith in scientific progress was great, and a structure depicting [[atom]]s was chosen to highlight humanity's advance in science, including [[nuclear physics]], which Belgium itself was an avid practitioner ever since the [[Belgian Congo]]'s [[uranium]] became the dominant source of material used for [[nuclear fission]], including that used for the world's first [[nuclear weapon]]s.<ref>{{Cite web |last=Swain |first=Frank |title=The forgotten mine that built the atomic bomb |url=https://www.bbc.com/future/article/20200803-the-forgotten-mine-that-built-the-atomic-bomb |access-date=2022-07-20 |website=www.bbc.com |language=en}}</ref> With the Atomium, Belgium wanted to highlight and promote the [[post-war]] ideal to peacefully apply atomic research and other advancements in technology in order to improve lives and serve the betterment of mankind.<ref>{{Cite web |title=Atomium: The Beginning of the Nuclear era |url=https://www.bie-paris.org/site/en/blog/entry/atomium-the-beginning-of-the-nuclear-era |access-date=2022-07-20 |website=www.bie-paris.org |language=en-gb}}</ref> The Atomium's nine {{convert|18|m|ft|adj=mid|-diameter}} [[stainless steel]] clad [[sphere]]s depict nine [[iron]] atoms in the shape of the body-centred cubic [[Crystal structure#Unit cell|unit cell]] that could for example represent α-iron ([[Allotropes of iron#Alpha iron (α-Fe)|ferrite]]) [[crystal]], magnified 165 billion times.<ref>{{cite web|title=History|url=http://atomium.be/History.aspx|publisher=Atomium Foundation|access-date=29 July 2016}}</ref>\n\n

                    The construction of the Atomium was a technical feat. In January 1955, a first project was presented by the engineer [[André Waterkeyn]], director of the economic department of {{ill|Fabrimétal|fr|Agoria (organization)}}; the Federation of Companies in the Metal Fabricating Industry (now known as [[Agoria]]). The architects André and Jean Polak were responsible for the architectural transposition of the concept, drawing up numerous sketches to do so. The company received assistance from consulting engineers Artémy S. Joukoff and André Beckers, assisted by the design office V. Daniel. The foundations were launched in March 1956 and the building, erected by the Jambes-Namur Construction Workshops, was completed less than a month before the inauguration of Expo '58, on 17 April 1958.<ref name=\":0\" />\n\n<gallery mode=\"packed\" heights=\"180\">\n
