                    ===Renovation (2004–2006)===\n
                    By the turn of the [[3rd millennium|millennium]], the state of the building had deteriorated and a comprehensive renovation was sorely needed. Renovation of the Atomium, carried by Belgian construction companies Jacques Delens and [[Besix|BESIX]],<ref>{{Cite web|title=Restauration de l'Atomium|url=http://www.beliris.be/projets/atomium.html|access-date=2021-01-14|website=Beliris|language=fr}}</ref> began in March 2004; it was closed to the public in October of that year, and remained closed until 18 February 2006. The renovation included replacing the faded [[aluminium]] sheets on the spheres with [[stainless steel]], as well as building a new reception pavilion with a [[boomerang]]-shaped roof. At the foot of the building, the [[roundabout]] was redeveloped into a concrete [[esplanade]] lined with continuous benches and a large step leading to the north-east.<ref name=\":0\" />\n\n

                    On 21 December 2005, the Atomium's new outdoor lighting was tested. The meridians of each sphere were covered with rectangular steel plates, in which [[LED lamp|LED lighting]] was integrated. The LED application illuminates the bulbs at night. The lights can also flash simultaneously or in turns at each meridian, symbolising the range of an [[electron]] around its core.<ref name=\":0\" /> In addition, the German industrial designer [[Ingo Maurer|Ingo Maure]] created lighting objects and installations for the interior of the building.<ref name=\"Hof\">{{cite news| last = Hof| first = Elisa von| url = https://www.spiegel.de/kultur/gesellschaft/ingo-maurer-der-lichtdesigner-ist-tot-a-1292711.html| title = Lichtdesigner Ingo Maurer ist tot| newspaper = [[Der Spiegel]]| date = 22 October 2019| language = de| access-date = 22 October 2019}}</ref>\n\n

                    On 14 February 2006, the Atomium was officially reopened by then-[[Philippe of Belgium|Prince Philippe]], and on 18 February 2006, it opened again to the public. The renovation cost €26 million. Brussels and the Atomium Association paid one-third of the costs, and the [[Federal Government of Belgium|Belgian Government]] financed the other two thirds. To help finance the renovation, pieces of the old aluminium plates were sold to the public as souvenirs. One triangular piece about {{convert|2|m|ft|sigfig=1}} long sold for €1,000.<ref>{{cite web |url=http://veerle-v2.duoh.com/blog/comments/the_atomium_restored/ |title=The Atomium restored |date=16 February 2006 |publisher=Veerle Pieters |access-date=20 June 2015 |archive-url=https://web.archive.org/web/20170923194316/http://veerle-v2.duoh.com/blog/comments/the_atomium_restored/ |archive-date=23 September 2017 |url-status=dead }}</ref> In March 2006, a [[2 euro commemorative coins#2006 coinage|2 euro commemorative coin]] depicting the building was issued to celebrate the renovation and reopening.\n\n

                    Though the Atomium depicts an iron unit cell, the spheres were originally clad with aluminium. Following the 2004–2006 renovation, however, the aluminium was replaced with stainless steel, which is primarily iron.<ref name=\":0\" /> Likewise, while the subject of Atomium was chosen to depict the enthusiasm of the [[Atomic Age]], iron is not and cannot be used as fuel in [[nuclear reaction]]s.\n\n

